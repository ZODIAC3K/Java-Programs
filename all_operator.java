public class all_operator {
    public static void main(String[] args) {
        // variable initilisation
        int a = 90, b = 10;
        
        // Addition Operator ('+')
        System.out.println("Addition Operator ('+') Example");
        System.out.println("a + b = " + (a + b));
        // Subtraction Operator ('-')
        System.out.println("Subtraction Operator ('-') Example");
        System.out.println("a - b = " + (a - b));
        // Multiplication Operator ('*')
        System.out.println("Multiplication Operator ('*') Example");
        System.out.println("a * b = " + (a * b));
        // Division Operator ('/')
        System.out.println("Division Operator ('/') Example");
        System.out.println("a / b = " + (a / b));
        // Modulo Operator ('%')
        System.out.println("Modulo Operator ('%') Example");
        System.out.println("a % b = " + (a % b));

    }
}
