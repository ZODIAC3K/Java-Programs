public class if_else_statement {
    public static void main(String[] args) {
        // String variable initilisation
        String language = "Java";
    
        // If statement
        if (language == "Java") {
          System.out.println("Java == Java");
        }
        // Else statement
        else {
            System.out.println("Java != java");
          }
      }
}
